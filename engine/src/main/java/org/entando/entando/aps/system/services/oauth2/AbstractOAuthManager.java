package org.entando.entando.aps.system.services.oauth2;

import com.agiletec.aps.system.SystemConstants;
import com.agiletec.aps.system.common.AbstractService;

public abstract class AbstractOAuthManager extends AbstractService {

    private Integer accessTokenValiditySeconds = SystemConstants.OAUTH2_ACCESS_TOKEN_DEFAULT_VALIDITY_SECOND;
    private Integer refreshTokenValiditySeconds = SystemConstants.OAUTH2_REFRESH_TOKEN_DEFAULT_VALIDITY_SECOND;

    protected Integer getAccessTokenValiditySeconds() {
        return accessTokenValiditySeconds;
    }

    public void setAccessTokenValiditySeconds(Integer accessTokenValiditySeconds) {
        this.accessTokenValiditySeconds = accessTokenValiditySeconds;
    }

    protected Integer getRefreshTokenValiditySeconds() {
        return refreshTokenValiditySeconds;
    }

    public void setRefreshTokenValiditySeconds(Integer refreshTokenValiditySeconds) {
        this.refreshTokenValiditySeconds = refreshTokenValiditySeconds;
    }

}
