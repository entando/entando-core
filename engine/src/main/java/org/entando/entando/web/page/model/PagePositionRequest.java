package org.entando.entando.web.page.model;

import org.hibernate.validator.constraints.NotBlank;

/**
 *
 * @author paddeo
 */
public class PagePositionRequest {

    @NotBlank(message = "page.code.NotBlank")
    private String code;
    @NotBlank(message = "parent.code.NotBlank")
    private String parentCode;
    private int position;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getParentCode() {
        return parentCode;
    }

    public void setParentCode(String parentCode) {
        this.parentCode = parentCode;
    }

    public int getPosition() {
        return position;
    }

    public void setPosition(int position) {
        this.position = position;
    }

    @Override
    public String toString() {
        return "PagePositionRequest{" + "code=" + code + ", parentCode=" + parentCode + ", position=" + position + '}';
    }
}
